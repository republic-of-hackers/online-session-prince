# Python Program to Check Whether a Number is Positive or Negative or Zero

# Procedures
# 1) analyse input in program.
# 2) analyse output in program.
# 3) analyse logic in program.

# analyse input - n
# analyse output - Positive | Negative
# analyse logic - n > 0 -> Positive | n < 0 -> Negative | n = 0 -> zero

# Program
n = int(input("Enter a number: "))
if n > 0:
    # ---- coding ----
    # LCM 
    print("Positive")
elif n == 0:
    print("Zero")
else:
    # ---- coding ----
    print("Negative")